<h3 class="card-title">
    Integración fácil de AdminLTE con Laravel
</h3>
<br/>
<p>
    Esta paquete proporciona una manera fácil de configurar rápidamente AdminLTE v3 con Laravel (7 o superior). No tiene
    otros requisitos y dependencias además de Laravel, por lo que puedes comenzar a construir tu panel de administración
    de inmediato. El paquete proporciona una plantilla blade que puedes extender y un sistema avanzado de configuración
    de menús. Además, opcionalmente, el paquete ofrece un conjunto de vistas de autenticación con estilo AdminLTE que
    puedes usar en reemplazo de las que proporciona el scaffolding de autenticación legacy de laravel/ui.
</p>
<p>
    Si deseas usar una versión anterior de Laravel o AdminLTE, revisa los siguientes lanzamientos del paquete:
</p>
<ul>
    <li>Lanzamientos 1.x: Estos lanzamientos soportan Laravel 5 e incluyen AdminLTE v2</li>
    <li>Lanzamientos 2.x: Estos lanzamientos soportan Laravel 6 e incluyen AdminLTE v2</li>
    <li>Lanzamientos 3.x (<=3.8.6): Estos lanzamientos soportan Laravel 6 e incluyen AdminLTE v3</li>
</ul>
<p>
    Toda la documentación está disponible en el sitio de <a href="https://github.com/jeroennoten/Laravel-AdminLTE/wiki">Documentación
        de Laravel-AdminLTE</a>, te animamos a leerla. Si eres nuevo, comienza con la <a
        href="https://github.com/jeroennoten/Laravel-AdminLTE/wiki/Installation">Guía de Instalación</a>. Para
    actualizar el paquete, consulta la <a href="https://github.com/jeroennoten/Laravel-AdminLTE/wiki/Updating">Guía de
        Actualización</a>.
</p>
<p>
    Los requisitos actuales del paquete son:
</p>
<ul>
    <li>Laravel >= 7.x</li>
    <li>PHP >= 7.2.5</li>
</ul>
<p>
    Puedes reportar problemas o hacer preguntas en la <a href="https://github.com/jeroennoten/Laravel-AdminLTE/issues">sección
        de issues</a>. Por favor, comienza tu issue con [BUG] y tu pregunta con [QUESTION] en el asunto.
</p>
<p>
    Si tienes una pregunta, se recomienda hacer una búsqueda en los issues cerrados primero.
</p>
<p>
    Para enviar un Pull Request, haz un fork de este repositorio y crea una nueva rama para comprometer tus nuevos
    cambios allí. Finalmente, abre un Pull Request desde tu nueva rama. Consulta las <a
        href="https://github.com/jeroennoten/Laravel-AdminLTE/blob/master/CONTRIBUTING.md">directrices de
        contribución</a> para obtener instrucciones detalladas. Al enviar un Pull Request, ten en cuenta las siguientes
    notas:
</p>
<ul>
    <li>Verifica que el Pull Request no introduzca una gran disminución en la calidad del código.</li>
    <li>Si el Pull Request agrega una nueva característica, considera agregar una propuesta de la documentación para
        esta característica también.
    </li>
    <li>Mantén el paquete enfocado, no agregues soporte especial a otros paquetes o para resolver situaciones muy
        particulares. Estos cambios harán que el paquete sea más difícil de mantener.
    </li>
</ul>
